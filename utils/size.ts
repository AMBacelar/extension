export type BrandKey = (typeof brandHash)[keyof typeof brandHash][number];

export type LegalSize = (typeof sizes)[number][BrandKey];

/**
 * Use this function only for vaidating sizes parsed out of emails
 * @param {string} brandName
 * @returns array of strings containing every valid size for the selected brand
 */
export const getLegalSizes = (brandName: keyof typeof brandHash) => {
  const arrayOfPropertyNames = brandHash[brandName];
  const legalSizes: LegalSize[] = [];
  for (let i = 0; i < arrayOfPropertyNames.length; i++) {
    const propertyName = arrayOfPropertyNames[i];
    sizes.forEach((column) => {
      // note: colum refers to the index in the sizes array
      const item = column[propertyName];
      if (item) {
        if (Array.isArray(item)) {
          item.forEach((element) => legalSizes.push(element));
        } else if (typeof item === 'string') {
          legalSizes.push(item);
        }
      }
    });
  }
  return legalSizes;
};
